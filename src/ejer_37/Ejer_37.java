/*Recorrer todo el abecedario, utilizando letras may√∫sculas. Cada vez que
encuentre una vocal (A, E, I, O, U) , mostrarla en pantalla.*/

public class Ejer_37 {
	public static void main(String[] args) {

		for (char letra = 'A'; letra <= 'Z'; letra++) {

			if (letra == 'A') {
				System.out.println("Se encontro la vocal: " + letra);
			}

			if (letra == 'E') {
				System.out.println("Se encontro la vocal: " + letra);
			}

			if (letra == 'I') {
				System.out.println("Se encontro la vocal: " + letra);
			}

			if (letra == 'O') {
				System.out.println("Se encontro la vocal: " + letra);
			}

			if (letra == 'U') {
				System.out.println("Se encontro la vocal: " + letra);
			}

		}

	}
}
